import turtle
turtle.setup(600,600)
turtle.begin_fill()
turtle.fillcolor("green")
turtle.circle(60,360)
turtle.end_fill()
turtle.penup()
turtle.left(90)
turtle.fd(120)
turtle.right(90)
turtle.pendown()
turtle.fd(90)
turtle.right(90)
turtle.fd(120)
turtle.right(90)
turtle.fd(180)
turtle.right(90)
turtle.fd(120)
turtle.right(90)
turtle.fd(90)
turtle.exitonclick()
