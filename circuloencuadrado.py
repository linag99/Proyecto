import turtle
turtle.setup(600,600)
turtle.begin_fill()
turtle.fillcolor("orange")
turtle.circle(60,360)
turtle.end_fill()
turtle.penup()
turtle.left(90)
turtle.fd(120)
turtle.right(90)
turtle.pendown()
turtle.fd(60)
turtle.right(90)
turtle.fd(120)
turtle.right(90)
turtle.fd(120)
turtle.right(90)
turtle.fd(120)
turtle.right(90)
turtle.fd(60)
turtle.exitonclick()
